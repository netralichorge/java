class Datatypes
{
	public static void main(String args[])
	{
		// PRIMITIVE DATATYPES / primary dataypes
		// Because of these primitive datatypes java is not object oriented language
		
		byte a=112;
		System.out.println("Value of a is "+a);
		System.out.println("**********"+a+"*********");
		System.out.println(a+" is stored in a variable");
		
		short b=1000;
		System.out.println("b="+b);
		
		int c=568980;
		System.out.println(c);
		
		float d=12.96f;
		System.out.println("Value of d is "+d);
		
		long e=12452334782467l;
		System.out.println("e="+e);
		
		double f=23.983455;
		System.out.println(f+" is stored in f variable");
		
		char g='@';
		System.out.println(g);
		
		boolean h=true;
		System.out.println(h);
	}
}

