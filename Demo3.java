class Demo3
{
	public static void main(String args[])
	{
		// Ternary operator
		/* If condition is true then print first statement and 
		   if condition is false then print second statement 
		*/
		
		int a=67;
		int b=30;
		
		System.out.println(a>b?"a is greater":"b is greater");
		
		int p=670;
		int q=300;
		
		int biggerValue=p>q?p:q;
	}
	
}