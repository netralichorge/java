// Array is collection of similar datatype

class Array
{
	public static void main(String args[])
	{
		int numbers[]=new int[3];
		//25  19  28
		//0   1   2
		
		numbers[0]=25;
		numbers[1]=19;
		numbers[2]=28;
		
		System.out.println(numbers[0]);
		System.out.println(numbers[1]);
		System.out.println(numbers[2]);
		
	}
}